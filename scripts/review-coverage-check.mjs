#!/usr/bin/env node
/**
 * Deterministic reviewer full-text COVERAGE CHECKER (audit A-8).
 *
 * The triad release-review protocol tells every reviewer to read the FULL
 * CURRENT TEXT of every changed file. Before this gate, `buildTouchedFilePack`
 * silently dropped files past a byte budget with only a disclosed "omission
 * note" — so on a large phase (Ф3: ~157 changed source files, ~3.68MB) the
 * reviewers did NOT actually receive every changed file's full text. A
 * disclosed omission is not a guarantee.
 *
 * This checker BLOCKS the seal unless every changed HAND-WRITTEN source file's
 * complete current bytes appear in at least one supplied touched-file pack. It
 * is run over ALL packet-split sub-wave packs at once; the union of what those
 * packs cover must equal the full changed-file set.
 *
 * Coverage mechanism (byte-exact, never fuzzy):
 *   A file P with current text T is COVERED only if some pack contains the
 *   exact section `### P\n\n```\nT\n```` that buildTouchedFilePack emits — i.e.
 *   the file's complete current bytes, fenced, under its stable header. A
 *   truncated/omitted file has no such section (only an OMISSION NOTE entry or
 *   a header with different bytes) and is reported NOT covered.
 *
 * Classification: every changed file is HAND-WRITTEN SOURCE (requires full-text
 * coverage) unless it is DIFF-AUTHORITATIVE — a generated or fixture artifact
 * whose diff is authoritative and whose full text adds no review value:
 *   - packages/schema/generated/**          (generated JSON Schemas)
 *   - docs/reference/endpoints.json         (generated by gen-endpoints-doc)
 *   - apps/macos/**\/Tests/**\/Fixtures/wire/**  (swift wire fixtures)
 *   - packages/harness-*\/fixtures/**        (harness transcript fixtures)
 *   - a small documented allowlist of generated artifacts (see
 *     GENERATED_ARTIFACT_ALLOWLIST below).
 * Deleted files (no current text) never require coverage.
 *
 * Usage:
 *   node scripts/review-coverage-check.mjs \
 *     --base <sha> --candidate <sha> \
 *     --pack <triad-prompt.md> [--pack <scope-prompt.md> ...] [--json]
 *
 * Exit 0: every hand-written changed file is fully covered.
 * Exit 1: one or more hand-written files are uncovered (listed precisely).
 * Exit 2: bad invocation.
 */

import { execFileSync } from "node:child_process";
import { readFileSync } from "node:fs";

import {
  touchedFileSection,
  touchedFileHeader,
  TOUCHED_FILE_OMISSION_MARKER,
} from "./lib/release-review-contract.mjs";

const SHA1 = /^[0-9a-f]{40}$/;

// Diff-authoritative classification rules. Everything NOT matched here is
// HAND-WRITTEN SOURCE and requires full-text coverage.
const SWIFT_WIRE_FIXTURE =
  /^apps\/macos\/[^/]+(?:\/[^/]+)*\/Tests\/[^/]+(?:\/[^/]+)*\/Fixtures\/wire\/.+/;
const HARNESS_FIXTURE = /^packages\/harness-[^/]+\/fixtures\/.+/;

/**
 * Small, documented allowlist of fully machine-generated artifacts that are
 * reviewed via their diff (their generator is the source of truth):
 *   - pnpm-lock.yaml                                   generated by pnpm
 *   - packages/util/src/version.ts                     generated by gen-version.mjs
 *   - apps/macos/.../HarnessLogoData.swift             auto-generated brand path data
 */
export const GENERATED_ARTIFACT_ALLOWLIST = Object.freeze([
  "pnpm-lock.yaml",
  "packages/util/src/version.ts",
  "apps/macos/ClaudexorApp/Sources/ClaudexorApp/HarnessLogoData.swift",
]);

/**
 * Classify a changed path. Returns null when the file is hand-written source
 * (requires full-text coverage) or a short rule id when it is diff-authoritative.
 */
export function diffAuthoritativeRule(path, allowlist = GENERATED_ARTIFACT_ALLOWLIST) {
  if (path.startsWith("packages/schema/generated/")) return "generated-schema";
  if (path === "docs/reference/endpoints.json") return "generated-endpoints-json";
  if (SWIFT_WIRE_FIXTURE.test(path)) return "swift-wire-fixture";
  if (HARNESS_FIXTURE.test(path)) return "harness-fixture";
  if (allowlist.includes(path)) return "generated-artifact-allowlist";
  return null;
}

/** The exact bytes a full-text-covered file contributes to a pack. */
export function coverageNeedle(path, currentText) {
  return touchedFileSection(path, currentText);
}

/**
 * Decide whether a single hand-written file's COMPLETE current text is present
 * in at least one pack. `reason` explains an uncovered verdict precisely.
 */
export function fileCoverage(path, currentText, packContents) {
  const needle = coverageNeedle(path, currentText);
  for (const pack of packContents) {
    if (pack.includes(needle)) return { covered: true, reason: null };
  }
  // Not fully present — classify WHY for an actionable report.
  const header = touchedFileHeader(path);
  const omissionEntry = `${TOUCHED_FILE_OMISSION_MARKER}`;
  for (const pack of packContents) {
    if (pack.includes(omissionEntry) && pack.includes(`${path} (`)) {
      return {
        covered: false,
        reason: "listed in a pack OMISSION NOTE (omitted past byte budget)",
      };
    }
  }
  for (const pack of packContents) {
    if (pack.includes(header)) {
      return {
        covered: false,
        reason: "section header present but full current bytes are truncated/altered",
      };
    }
  }
  return { covered: false, reason: "no full-text section in any supplied pack" };
}

/**
 * Pure coverage engine. `files` is [{ path, deleted }]; `readCurrentText(path)`
 * returns the candidate's current bytes for a live file. Returns a structured
 * report; `ok` is false iff any hand-written live file is uncovered.
 */
export function checkCoverage({ files, readCurrentText, packContents, allowlist }) {
  const covered = [];
  const uncovered = [];
  const skipped = []; // diff-authoritative
  const deleted = [];
  for (const file of files) {
    if (file.deleted) {
      deleted.push(file.path);
      continue;
    }
    const rule = diffAuthoritativeRule(file.path, allowlist ?? GENERATED_ARTIFACT_ALLOWLIST);
    if (rule) {
      skipped.push({ path: file.path, rule });
      continue;
    }
    const text = readCurrentText(file.path);
    const result = fileCoverage(file.path, text, packContents);
    if (result.covered) covered.push(file.path);
    else uncovered.push({ path: file.path, reason: result.reason });
  }
  return { ok: uncovered.length === 0, covered, uncovered, skipped, deleted };
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

function parseArgs(argv) {
  const packs = [];
  let base = null;
  let candidate = null;
  let json = false;
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--pack") packs.push(argv[++i]);
    else if (a === "--base") base = argv[++i];
    else if (a === "--candidate") candidate = argv[++i];
    else if (a === "--json") json = true;
    else return { error: `unknown or malformed argument: ${a}` };
  }
  if (!base || !SHA1.test(base))
    return { error: "--base must be a full lowercase 40-char commit SHA" };
  if (!candidate || !SHA1.test(candidate)) {
    return { error: "--candidate must be a full lowercase 40-char commit SHA" };
  }
  if (packs.length === 0 || packs.some((p) => !p))
    return { error: "at least one --pack <file> is required" };
  return { base, candidate, packs, json };
}

function git(args) {
  return execFileSync("git", args, { encoding: "utf8", maxBuffer: 256 * 1024 * 1024 });
}

/** Enumerate changed files (base..candidate), marking deletions. */
function changedFiles(base, candidate) {
  const out = git(["diff", "--name-status", `${base}..${candidate}`]).trim();
  if (!out) return [];
  const files = [];
  for (const line of out.split("\n")) {
    const parts = line.split("\t");
    const status = parts[0];
    if (status.startsWith("R") || status.startsWith("C")) {
      // rename/copy: the new path (last field) carries the current bytes.
      files.push({ path: parts[parts.length - 1], deleted: false });
    } else if (status === "D") {
      files.push({ path: parts[1], deleted: true });
    } else {
      files.push({ path: parts[1], deleted: false });
    }
  }
  return files;
}

function main() {
  const parsed = parseArgs(process.argv.slice(2));
  if (parsed.error) {
    console.error(`review-coverage-check: ${parsed.error}`);
    process.exit(2);
  }
  const { base, candidate, packs, json } = parsed;
  const packContents = packs.map((p) => readFileSync(p, "utf8"));
  const files = changedFiles(base, candidate);
  const report = checkCoverage({
    files,
    readCurrentText: (path) => git(["show", `${candidate}:${path}`]),
    packContents,
  });

  if (json) {
    console.log(
      JSON.stringify(
        {
          ok: report.ok,
          base,
          candidate,
          packs,
          covered: report.covered,
          uncovered: report.uncovered,
          diffAuthoritativeSkips: report.skipped,
          deleted: report.deleted,
        },
        null,
        2,
      ),
    );
  } else {
    console.error(
      `review-coverage-check: ${report.covered.length} hand-written file(s) fully covered, ` +
        `${report.skipped.length} diff-authoritative skip(s), ${report.deleted.length} deleted.`,
    );
    if (report.skipped.length > 0) {
      console.error("Diff-authoritative (reviewed via diff, full text not required):");
      for (const s of report.skipped) console.error(`  - ${s.path}  [${s.rule}]`);
    }
    if (!report.ok) {
      console.error(
        `\nCOVERAGE_BLOCKED: ${report.uncovered.length} hand-written file(s) NOT fully covered:`,
      );
      for (const u of report.uncovered) console.error(`  - ${u.path}  (${u.reason})`);
    } else {
      console.error(
        "\nCOVERAGE_OK: every hand-written changed file's full current text is covered.",
      );
    }
  }
  process.exit(report.ok ? 0 : 1);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
