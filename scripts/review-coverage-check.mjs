function parseArgs(argv) {
  const packs = [];
  let base = null;
  let candidate = null;
  let json = false;
  let wholeFileList = null;
  let receipt = null;
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--pack") {
      // `--pack <subwave>=<path>` labels a packet-split sub-wave's pack so the
      // receipt can bind panel slots to exact pack bytes; a bare `--pack
      // <path>` is the single unsplit wave (label "").
      const value = argv[++i] ?? "";
      const eq = value.indexOf("=");
      if (eq > 0 && /^[a-z0-9][a-z0-9-]{0,31}$/.test(value.slice(0, eq))) {
        packs.push({ subWave: value.slice(0, eq), path: value.slice(eq + 1) });
      } else {
        packs.push({ subWave: "", path: value });
      }
    } else if (a === "--base") base = argv[++i];
    else if (a === "--candidate") candidate = argv[++i];
    else if (a === "--json") json = true;
    else if (a === "--whole-file-list") wholeFileList = argv[++i];
    else if (a === "--receipt") receipt = argv[++i];
    else return { error: `unknown or malformed argument: ${a}` };
  }
  if (!base || !SHA1.test(base))
    return { error: "--base must be a full lowercase 40-char commit SHA" };
  if (!candidate || !SHA1.test(candidate)) {
    return { error: "--candidate must be a full lowercase 40-char commit SHA" };
  }
  if (packs.length === 0 || packs.some((p) => !p.path))
    return { error: "at least one --pack [subwave=]<file> is required" };
  const labels = packs.map((p) => p.subWave).filter(Boolean);
  if (new Set(labels).size !== labels.length) {
    return { error: "duplicate --pack sub-wave labels make the slot binding ambiguous" };
  }
  return { base, candidate, packs, json, wholeFileList, receipt };
}

function git(args) {
  return execFileSync("git", args, { encoding: "utf8", maxBuffer: 256 * 1024 * 1024 });
}

/**
 * Parse the NUL-delimited records of `git diff -z --name-status`. `-z` makes git
 * NUL-terminate every field (status AND each path get their OWN NUL — verified:
 * `A\0path\0`, not `A\tpath\0`) and disables path munging, so a path containing
 * a space, tab, newline, quote, or non-ASCII byte is preserved VERBATIM. The old
 * newline-split + tab-split parse silently mangled every such path (dropped or
 * misread the changed file, and would have split a legitimate tab-in-path). NUL
 * cannot occur in a POSIX filename, so it is the only safe delimiter. Field
 * stream (status is always a standalone token — a short A/M/D/T/R###/C### code):
 *   `<status>\0<path>\0`            A/M/D/T (add/modify/delete/typechange)
 *   `<Rxxx|Cxxx>\0<old>\0<new>\0`   rename/copy (the NEW path holds the bytes)
 * Parsing is POSITIONAL: status is only ever read at the record boundary, so a
 * path that itself begins with "R"/"C" (consumed at an odd position) is never
 * mistaken for a status.
 */
export function parseNameStatusZ(raw) {
  const fields = raw.split("\0").filter((f) => f !== ""); // drop the trailing NUL
  const files = [];
  for (let i = 0; i < fields.length;) {
    const status = fields[i];
    if (status.startsWith("R") || status.startsWith("C")) {
      // rename/copy carries old + new; the NEW path (2nd) has the current bytes.
      const newPath = fields[i + 2];
      if (newPath !== undefined) files.push({ path: newPath, deleted: false });
      i += 3;
    } else {
      const path = fields[i + 1];
      if (path !== undefined) files.push({ path, deleted: status === "D" });
      i += 2;
    }
  }
  return files;
}

/** Enumerate changed files (base..candidate), marking deletions. */
function changedFiles(base, candidate) {
  return parseNameStatusZ(git(["diff", "-z", "--name-status", `${base}..${candidate}`]));
}

/**
 * Union the changed set with a packet's FILES_TO_READ_WHOLE.txt: files that
 * did NOT change in base..candidate but whose FULL text the packet demands
 * (context folded into the base — the v3.0.1 r6 class). Without this union a
 * sub-wave selector that excludes a listed-but-unchanged file trips neither
 * the zero-match guard nor the changed-file coverage, and the file silently
 * loses full-text review.
 */
export function unionWithWholeFileList(files, listText) {
  if (!listText) return files;
  const union = [...files];
  const known = new Set(files.map((f) => f.path));
  for (const line of listText.split("\n")) {
    const path = line.trim();
    if (path && !path.startsWith("#") && !known.has(path)) {
      union.push({ path, deleted: false });
      known.add(path);
    }
  }
  return union;
}

/**
 * Candidate-bound machine receipt for the seal (audit A-8): the sealed
 * attestation embeds this body's file digest, so the union-coverage proof is
 * signature-bound rather than a claim in prose. Each pack carries its
 * SUB-WAVE label so the seal can bind panel slots to the exact pack bytes
 * their reviewers consumed (an unlabeled single-wave pack uses "").
 */
export function coverageReceiptBody(
  report,
  { base, candidate, packs, packContents, wholeFileList },
) {
  return {
    schemaVersion: 1,
    ok: report.ok,
    base,
    candidate,
    packs: packs.map((pack, index) => ({
      subWave: pack.subWave,
      path: pack.path,
      sha256: createHash("sha256").update(packContents[index]).digest("hex"),
    })),
    wholeFileList: wholeFileList ?? null,
    covered: report.covered.length,
    uncovered: report.uncovered,
    diffAuthoritativeSkips: report.skipped.length,
    deleted: report.deleted.length,
  };
}

/**
 * The ONE coverage computation both the CLI and the attestation sealer run:
 * enumerate base..candidate (-z), union the packet's whole-file list, check
 * byte-exact coverage across the pack files, and return the report plus the
 * receipt body derived from the SAME bytes. The sealer calls this instead of
 * trusting a caller-supplied receipt (a hand-authored ok:true must not seal).
 */
export function runCoverage({ base, candidate, packs, wholeFileListPath }) {
  const packContents = packs.map((pack) => readFileSync(pack.path, "utf8"));
  const files = unionWithWholeFileList(
    changedFiles(base, candidate),
    wholeFileListPath ? readFileSync(wholeFileListPath, "utf8") : null,
  );
  const report = checkCoverage({
    files,
    readCurrentText: (path) => git(["show", `${candidate}:${path}`]),
    packContents,
  });
  return {
    report,
    receiptBody: coverageReceiptBody(report, {
      base,
      candidate,
      packs,
      packContents,
      wholeFileList: wholeFileListPath ?? null,
    }),
  };
}

function main() {
  const parsed = parseArgs(process.argv.slice(2));
  if (parsed.error) {
    console.error(`review-coverage-check: ${parsed.error}`);
    process.exit(2);
  }
  const { base, candidate, packs, json, wholeFileList, receipt } = parsed;
  const { report, receiptBody } = runCoverage({
    base,
    candidate,
    packs,
    wholeFileListPath: wholeFileList,
  });
  if (receipt) {
    writeFileSync(receipt, `${JSON.stringify(receiptBody, null, 2)}\n`);
  }

  if (json) {
    console.log(
      JSON.stringify(
        {
          ok: report.ok,
          base,
          candidate,
          packs,
          covered: report.covered,
          uncovered: report.uncovered,
          diffAuthoritativeSkips: report.skipped,
          deleted: report.deleted,
        },
        null,
        2,
      ),
    );
  } else {
    console.error(
      `review-coverage-check: ${report.covered.length} hand-written file(s) fully covered, ` +
        `${report.skipped.length} diff-authoritative skip(s), ${report.deleted.length} deleted.`,
    );
    if (report.skipped.length > 0) {
      console.error("Diff-authoritative (reviewed via diff, full text not required):");
      for (const s of report.skipped) console.error(`  - ${s.path}  [${s.rule}]`);
    }
    if (!report.ok) {
      console.error(
        `\nCOVERAGE_BLOCKED: ${report.uncovered.length} hand-written file(s) NOT fully covered:`,
      );
      for (const u of report.uncovered) console.error(`  - ${u.path}  (${u.reason})`);
    } else {
      console.error(
        "\nCOVERAGE_OK: every hand-written changed file's full current text is covered.",
      );
    }
  }
  process.exit(report.ok ? 0 : 1);
}

/**
 * Embedded negative control (staged-field-check pattern): prove the `-z` parser
 * still discriminates the path classes that the old newline/tab split mangled,
 * BEFORE trusting any real verdict. Runs on every invocation.
 */
function selfTest() {
  // Exact `git diff -z --name-status` framing: NUL after status and after each
  // path; a rename carries two paths. Paths exercise a space, unicode, a literal
  // TAB, and an embedded NEWLINE — every class the old line/tab split mangled. A
  // path beginning with "R" proves the positional parse never reads it as a
  // rename status.
  const raw =
    "A\0plain.txt\0" +
    "M\0dir/café file.txt\0" +
    "D\0old gone.txt\0" +
    "R100\0src/before.ts\0src/after café.ts\0" +
    "M\0has\ttab.txt\0" +
    "A\0line\none.txt\0" +
    "M\0Rocket.md\0";
  const got = parseNameStatusZ(raw);
  const expected = [
    { path: "plain.txt", deleted: false },
    { path: "dir/café file.txt", deleted: false },
    { path: "old gone.txt", deleted: true },
    { path: "src/after café.ts", deleted: false },
    { path: "has\ttab.txt", deleted: false },
    { path: "line\none.txt", deleted: false },
    { path: "Rocket.md", deleted: false },
  ];
  if (JSON.stringify(got) !== JSON.stringify(expected)) {
    console.error(
      "review-coverage-check SELF-TEST FAILED: the -z name-status parser no longer " +
        `handles space/unicode/tab/newline/rename paths.\n  expected: ${JSON.stringify(expected)}\n  got:      ${JSON.stringify(got)}`,
    );
    process.exit(2);
  }
}

if (import.meta.url === `file://${process.argv[1]}`) {
  selfTest();
  main();
}
